const GOAL_TO_CATEGORY: Record<string, string[]> = {
  'overall-health': ['overall-health'],
  'boost-immunity': ['boost-immunity', 'immune-support'],
  'energy-vitality': ['energy-vitality', 'low-energy'],
  'bone-joint': ['bone-joint', 'joint-discomfort'],
  'heart-health': ['heart-health'],
  'gut-health': ['gut-health', 'digestive-sensitivity'],
  'sleep-stress': ['sleep-stress', 'sleep-quality', 'stress-anxiety'],
  'brain-health': ['brain-health'],
}

// Map quiz concerns to product categories
const CONCERN_TO_CATEGORY: Record<string, string[]> = {
  'sleep-quality': ['sleep-stress', 'sleep-quality'],
  'low-energy': ['energy-vitality', 'low-energy'],
  'digestive-sensitivity': ['gut-health', 'digestive-sensitivity'],
  'joint-discomfort': ['bone-joint', 'joint-discomfort'],
  'stress-anxiety': ['sleep-stress', 'stress-anxiety'],
  'immune-support': ['boost-immunity', 'immune-support'],
  'weight-management': ['weight-management'],
  'none': ['overall-health'],
}

// Get results based on quiz params (goals and concerns)
export function getResultsByQuizParams(
  goals: string,
  concerns: string
): MockResult[] {
  // Get categories from goals
  const goalCategories = GOAL_TO_CATEGORY[goals] || ['overall-health']
  
  // Get categories from concerns
  const concernCategories = CONCERN_TO_CATEGORY[concerns] || []
  
  // Combine all relevant categories
  const allCategories = [...new Set([...goalCategories, ...concernCategories])]
  
  // Filter products that match any of the categories
  const matchedProducts = ALL_PRODUCTS.filter(product =>
    product.category.some(cat => allCategories.includes(cat))
  )
  
  // Sort by rating (highest first), then by segment priority
  const segmentPriority: Record<string, number> = { top: 0, premium: 1, value: 2, budget: 3 }
  
  return matchedProducts.sort((a, b) => {
    // First by segment
    const segDiff = segmentPriority[a.segment] - segmentPriority[b.segment]
    if (segDiff !== 0) return segDiff
    // Then by rating
    return b.rating - a.rating
  })
}

// Legacy function - Filter results by segment (for backward compatibility)
export function getResultsBySegment(segment: 'top' | 'budget' | 'premium' | 'value' | 'all'): MockResult[] {
  if (segment === 'all') return ALL_PRODUCTS
  if (segment === 'value') {
    // Value = has sale price or value segment
    return ALL_PRODUCTS.filter(r => r.originalPrice || r.segment === 'value')
  }
  return ALL_PRODUCTS.filter(r => r.segment === segment)
}

// For backward compatibility
export const MOCK_RESULTS = ALL_PRODUCTS
